    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed

// get input from user ;

        String FN = Fname.getText();
        String LN = Lname.getText();
        String PN = Pnum.getText();
        String G  = Gender ;
        
       
    // Check if any field is empty
    if (FN.isEmpty()) {
        LabelFname.setText("Please enter First Name");
        return; // Exit the method if First Name is empty
    }
    if (LN.isEmpty()) {
        LabelLname.setText("Please enter Last Name");
        return; // Exit the method if Last Name is empty
    }
    if (PN.isEmpty() || PN.length() < 10) {
            LabelPnum.setText("Please enter Correct Phone Number"); // Display error message
        return; // Exit the method if Phone Number is empty
    }
    if (G == null) {
        jLabelgender.setText("Please select Gender");
        return; // Exit the method if Gender is not selected
    }
        
// show windo for cinform :
       int option = JOptionPane.showConfirmDialog(null, "Successful \n"+"name:"+ FN +" "+ LN + "\nPhone Number:" + PN+ "\nGender:"+G , "Submation", JOptionPane.OK_CANCEL_OPTION);
   
// set data into database wen press ok :
    if (option == JOptionPane.OK_OPTION){
         try 
         {
             pst = con.prepareStatement(
                       "INSERT INTO customer "
                     + "(C_Fname,C_Lname,C_PhoneNo,Gender)"
                     + " VALUES (?,?,?,?)");
             
             
             pst.setString(1, FN);
             pst.setString(2, LN);            
             pst.setString(3, PN);
             pst.setString(4,  Short_Gender );
             
           
             
             pst.executeUpdate();

            this.dispose();
         
         }// end try 
         catch(SQLException ex)
         {
             JOptionPane.showConfirmDialog(null, ex.getMessage() , "Error database", JOptionPane.ERROR_MESSAGE);

         }// end catch
        
       
    }// end if
       

        
    }//GEN-LAST:event_SubmitActionPerformed

    
