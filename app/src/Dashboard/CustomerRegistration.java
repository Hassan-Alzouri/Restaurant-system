    private void PnumKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PnumKeyPressed
        
    
  // Get the current text in the JTextField
    String phoneNumber = Pnum.getText();
    
    // Get the length of the current text
    int length = phoneNumber.length();
    
    // Get the pressed key
    char c = evt.getKeyChar();

    // Check if the pressed key is a digit (0-9)
    if (Character.isDigit(c)) {
        // Allow input if length is less than 10
        if (length < 10) {
            Pnum.setEditable(true);
            LabelPnum.setText(""); // Clear any previous error message
        } else {
            // Disallow input if length is already 10
            Pnum.setEditable(false);
            LabelPnum.setText("Maximum 10 digits allowed"); // Display error message
        }
    } else if (evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
        // Allow backspace key
        Pnum.setEditable(true);
        LabelPnum.setText(""); // Clear any previous error message
    } else {
        // Disallow input for non-digit characters
        Pnum.setEditable(false);
        LabelPnum.setText("Only digits allowed"); // Display error message
    }


    }//GEN-LAST:event_PnumKeyPressed
    
    
